'''
Impossivel
Leu o título e achou que o problema ia ser difícil? Pois achou errado. Leia um inteiro n e imprima n vezes a frase "O impossivel so e impossivel quando nao acreditamos mais no possivel :bless:".

A Entrada consiste de:
Uma única linha, contendo um inteiro positivo n, (2≤n≤100).

A Saída deve apresentar:
n vezes a frase pedida no enunciado.

Observações:
Não é necessário validar se os valores de entrada estão dentro dos intervalos e tipos definidos.

Descrição dos Exemplos:
No primeiro exemplo, a resposta é "O impossivel so e impossivel quando nao acreditamos mais no possivel :bless:", pois n=1.
'''

n = int(input())

variavel_controle = 0

while n <= variavel_controle:
    print('O impossivel so e impossivel quando nao acreditamos mais no possivel :bless:')
    n += 1
